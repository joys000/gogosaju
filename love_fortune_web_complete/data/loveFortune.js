function getRandomItems(arr, n) {
  const shuffled = [...arr].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, n);
}

function getCombinedFortune(month, positivity = 3) {
  const monthKey = `${month}월`;
  const positive = getRandomItems(positiveFortune[monthKey] || [], positivity);
  const negative = getRandomItems(negativeFortune[monthKey] || [], 5 - positivity);
  return [
    `🌟 ${month}월의 연애운은 활발한 기운이 있어요!`,
    ...positive,
    `⚠️ 동시에 주의할 점도 있어요. 감정 기복에 유의하세요.`,
    ...negative
  ];
}

export { positiveFortune, negativeFortune, getCombinedFortune };
